#include <stdio.h>


/**
 *  * main - print the letters of the alphabet
 *   * Description: print the letters of the alphabet in lowercase\n
 *    * Return: Always 0 (Success)
 *     */

int main(void)
{
		int ch;

			for (ch = 'a'; ch <= 'z'; ch++)
					{
								putchar(ch);

									}
				putchar('\n');

					return (0);
}
